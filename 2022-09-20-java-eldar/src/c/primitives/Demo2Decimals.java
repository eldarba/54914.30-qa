package c.primitives;

public class Demo2Decimals {

	public static void main(String[] args) {

		float f1 = 5.19F;
		double f2 = 5.36;

		System.out.println(f1);
		System.out.println(f2);

		System.out.println(5);
		System.out.println(5D);
		
		// double 64 bit - higher precision
		System.out.println(0.333333333333333333333333333D);
		// float 32 bit - lower precision
		System.out.println(0.333333333333333333333333333F);
	}

}
